#include <stdio.h>
#include <string.h>                                    // 문자열을 다룰 수 있는 string.h 헤더 파일 포함

int main(void)
{
	char fruit[7] = "banana";                          // banana로 초기화

	printf("%s\n", fruit);                             // banana 출력
	strcpy(fruit, "tomato");                           // fruit에 tomato 복사
	printf("%s\n", fruit);                             // tomato 출력

	return 0;
}